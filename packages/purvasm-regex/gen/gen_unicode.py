"""Generate src/Regex/Core/Unicode.purs (ADR-0081 §1): flat [lo,hi,...] code-point range
tables for the five demanded general categories, from Python's bundled Unicode database.
Regeneration: python3 gen/gen_unicode.py > src/Regex/Core/Unicode.purs (diffable, committed)."""
import sys, unicodedata

CATS = {
    "lu": lambda c: c == "Lu",
    "ll": lambda c: c == "Ll",
    "letter": lambda c: c.startswith("L"),
    "punct": lambda c: c.startswith("P"),
    "symbol": lambda c: c.startswith("S"),
}

def ranges(pred):
    out, lo = [], None
    for cp in range(0x110000):
        if pred(unicodedata.category(chr(cp))):
            if lo is None:
                lo = cp
        elif lo is not None:
            out.append((lo, cp - 1)); lo = None
    if lo is not None:
        out.append((lo, 0x10FFFF))
    return out

